    class func CarePortalMedium(size : CGFloat) -> UIFont {
        return UIFont(name: "Roboto-Medium", size: size)!
    }
    class func CarePortalRegular(size : CGFloat) -> UIFont {
        return UIFont(name: "Roboto-Regular", size: size)!
    }
    class func latoBlack(size : CGFloat) -> UIFont {
        return UIFont(name: "Lato-Black", size: size)!
    }
    class func latoRegular(size : CGFloat) -> UIFont {
        return UIFont(name: "Lato-Regular", size: size)!
    }
    class func latoBold(size : CGFloat) -> UIFont {
